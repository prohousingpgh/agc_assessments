"""
Generate the LaTeX data fragments for the AGC reassessment report.

NO numbers are hand-written in the report. This script reads the pipeline's
own outputs and emits, into report/generated/:
  - macros.tex          : \newcommand definitions for every inline number
  - results_table.tex   : the per-group MSR / COD table (existing vs new)
  - equity_income.tex   : MSR / COD by Census median-income quintile
  - equity_minority.tex : MSR / COD by Census-tract pct_minority band

report.tex \input's these, so every figure in the prose and tables traces
back to a generated value. Re-run this whenever the models/ratio study change:
    python report/build_report.py

Sources (all generated by the pipeline, none hand-edited):
  - data/.../out/models/<group>/reports/ratio_study.md   (MSR, COD, breakdowns)
  - data/.../out/models/<group>/main/model_ensemble.pickle (VEI)
  - output/residential_predictions.csv                    (county under-assessment)
"""
