# --- parse the canonical ratio-study markdown report ---------------------------

def _report_text(group):
    p = os.path.join(MODELS, group, "reports", "ratio_study.md")
    return open(p, encoding="utf-8").read() if os.path.exists(p) else ""

def _overall(text, trimmed=True):
    """(county_MSR, our_MSR, county_COD, our_COD) from the improved-overall block."""
    tag = r"Improved property only \(trimmed\)" if trimmed else r"Improved property only \(untrimmed\)"
    m = re.search(tag + r"(.*?)(?:####|\Z)", text, re.S)
    if not m:
        return (None, None, None, None)
    b = m.group(1)
    mr = re.search(r"Median ratio \|\s*([\d.]+)\s*\|\s*([\d.]+)", b)
    cod = re.search(r"\| COD \|\s*([\d.]+)\s*\|\s*([\d.]+)", b)
    cnt = re.search(r"\| Count \|\s*([\d,]+)\s*\|\s*([\d,]+)", b)
    return (mr and mr.group(1), mr and mr.group(2), cod and cod.group(1), cod and cod.group(2),
            cnt and cnt.group(2))

def _breakdown_rows(text, by):
    """[(label, count, our_MSR, our_COD_trim)] for a breakdown under 'Our results'."""
    sub = text[text.find("### Our results"):]
    m = re.search(rf"#### By {by}.*?(?=\n#### |\n### |\Z)", sub, re.S)
    if not m:
        return []
    out = []
    for line in m.group(0).splitlines():
        cells = [c.strip() for c in line.split("|")[1:-1]]
        # a data row has a numeric Count in column 2; this excludes the header
        # row, the |---| separator, and the "N other groups with count < 15"
        # catch-all row.
        if len(cells) >= 6 and cells[1].replace(",", "").isdigit() and "other group" not in cells[0].lower():
            label = cells[0].split("<br>")[0].strip()
            out.append((label, cells[1], cells[2], cells[5]))
    return out


def _esc(s):
    """Escape LaTeX specials in extracted label text (e.g. the % in '75-100%')."""
    for a, b in [("%", r"\%"), ("&", r"\&"), ("_", r"\_"), ("#", r"\#"), ("$", r"\$")]:
        s = s.replace(a, b)
    return s

# --- VEI from the trained ensemble pickles -------------------------------------

def _vei(group):
    import pickle
    from openavmkit.vertical_equity_study import get_vertical_equity_scores
    p = os.path.join(MODELS, group, "main", "model_ensemble.pickle")
    if not os.path.exists(p):
        return None
    res = pickle.load(open(p, "rb"))
    df = getattr(res, "df_test", None)
    if df is None or "sale_price_time_adj" not in df.columns or "prediction" not in df.columns:
        return None
    d = pd.DataFrame({"s": df["sale_price_time_adj"].values, "v": df["prediction"].values})
    d = d[np.isfinite(d["s"]) & (d["s"] > 0) & np.isfinite(d["v"])]
    try:
        return get_vertical_equity_scores(d, "s", "v")["vei"]
    except Exception:
        return None

# --- county-wide under-assessment from the published predictions ---------------

def _county_underassessment():
    p = os.path.join(REPO, "output", "residential_predictions.csv")
    if not os.path.exists(p):
        return None, None
    df = pd.read_csv(p)
    new = pd.to_numeric(df["NEW_ASSESSMENT_TOTAL"], errors="coerce").sum()
    cur = pd.to_numeric(df["CURRENT_ASSESSMENT_TOTAL"], errors="coerce").sum()
    ratio = new / cur if cur else None
    return ratio, (100.0 / ratio if ratio else None)  # ratio (e.g. 2.08), pct-of-market (e.g. 48)

# --- dataset-quality audit from the 100-parcel sanity-check sample -------------

def _dataset_audit():
    """Regenerate the parcel-data error rates from the committed sanity-check CSV.

    Compares the county's listed values against independent references (computed
    parcel geometry for lot area; Zillow for the rest) for a 100-parcel sample.
    Returns (macro_lines, audit_table_tex) or ([], None) if the file is absent.
    """
    p = os.path.join(REPO, "output", "allegheny_county_parcel_data_sanity_check.csv")
    if not os.path.exists(p):
        return [], None
    df = pd.read_csv(p)
    n = len(df)

    def num(c):
        return pd.to_numeric(df[c], errors="coerce") if c in df.columns else pd.Series(dtype=float)

    def pct_bands(county, ref):
        m = county.notna() & ref.notna() & (ref > 0)
        pe = (county[m] - ref[m]).abs() / ref[m]
        return int(((pe > 0.10) & (pe <= 0.30)).sum()), int((pe > 0.30).sum())

    def int_bands(county, ref):
        m = county.notna() & ref.notna()
        d = (county[m] - ref[m]).abs()
        return int((d == 1).sum()), int((d > 1).sum())

    def differ(county, ref):
        m = county.notna() & ref.notna()
        return int((county[m] != ref[m]).sum())

    lot_mod, lot_maj = pct_bands(num("LOTAREA"), num("Computed lot size"))
    yr_one, yr_multi = int_bands(num("YEARBLT"), num("Zillow year built"))
    fla_mod, fla_maj = pct_bands(num("FINISHEDLIVINGAREA"), num("Zillow building square footage"))
    bed_one, bed_multi = int_bands(num("BEDROOMS"), num("Zillow bedroom count"))
    fullbath = differ(num("FULLBATHS"), num("Zillow full baths count"))
    halfbath = differ(num("HALFBATHS"), num("Zillow half baths count"))

    m = [
        rf"\newcommand{{\auditSampleN}}{{{n}}}",
        rf"\newcommand{{\auditLotMod}}{{{lot_mod}}}",
        rf"\newcommand{{\auditLotMaj}}{{{lot_maj}}}",
        rf"\newcommand{{\auditLotAny}}{{{lot_mod + lot_maj}}}",
    ]
    rows = "\n".join([
        rf"Lot area & Computed geometry & off 10--30\% & {lot_mod} \\",
        rf"Lot area & Computed geometry & off $>$30\% & {lot_maj} \\",
        rf"Year built & Zillow & off by 1 year & {yr_one} \\",
        rf"Year built & Zillow & off by $>$1 year & {yr_multi} \\",
        rf"Finished living area & Zillow & off 10--30\% & {fla_mod} \\",
        rf"Finished living area & Zillow & off $>$30\% & {fla_maj} \\",
        rf"Bedrooms & Zillow & off by 1 & {bed_one} \\",
        rf"Bedrooms & Zillow & off by $>$1 & {bed_multi} \\",
        rf"Full baths & Zillow & count differs & {fullbath} \\",
        rf"Half baths & Zillow & count differs & {halfbath} \\",
    ])
    table = header + r"""\begin{tabular}{lllr}
\toprule
Field & Reference & Discrepancy & Parcels \\
\midrule
""" + rows + r"""
\bottomrule
\end{tabular}
"""
    return m, table

